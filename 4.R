#X~EXP(0.5),Y~EXP(0.5)#
n1=20
n2=20
mu=(n1*n2)/2
mu
Var=(n1*n2*(n1+n2+1)/12)
Var
s=sqrt(Var)
s
U_alpha=qnorm(0.95,mu,s)
U_alpha
set.seed(1234)
R=100000
L=function(n)
{
 X=rexp(n,rate=0.5)
 Y=rexp(n,rate=0.5)
 S=c(X,Y)
 r=rank(S)
 W=sum(r[(n+1):(n+n)])
 U=W-(n*(n+1)/2) 
 U
}
n=20
U=replicate(R,L(n))
t=length(U[U>=U_alpha])
t
p=t/R
p

