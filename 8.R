#Exponential Distribution# 
#Equal means#
set.seed(1234)
 R=100
 L=function(n)
 {
  X=rexp(n,rate=0.5)
  Y=rexp(n,rate=0.5)
  S=c(X,Y)
  r=rank(S)
  W=sum(r[(n+1):(n+n)])
  U=W-(n*(n+1)/2) 
  U
 }
 n=5
 U1=replicate(R,L(n))
shapiro.test(U1)

